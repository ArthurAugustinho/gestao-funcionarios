package com.ArthurAugustinho.gestaofuncionarios.auth.controller;

import com.ArthurAugustinho.gestaofuncionarios.auth.dto.LoginRequest;
import com.ArthurAugustinho.gestaofuncionarios.auth.dto.LoginResponse;
import com.ArthurAugustinho.gestaofuncionarios.auth.service.AuthService;
import jakarta.validation.Valid;
import lombok.RequiredArgsConstructor;
import org.springframework.http.ResponseEntity;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RestController;

@RestController
@RequestMapping("/api/auth")
@RequiredArgsConstructor
public class AuthController {

    private final AuthService authService;

    @PostMapping("/login")
    public ResponseEntity<LoginResponse> login(@Valid @RequestBody LoginRequest request) {
        return ResponseEntity.ok(authService.login(request));
    }
}
